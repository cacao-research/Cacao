/**
 * Get an SVG icon React element by name.
 * Falls back to the first character of the name as plain text.
 *
 * @param {string} name - Lucide-style icon name
 * @returns {React.Element|string}
 */
export function getIcon(name) {
  const factory = ICONS[name];
  if (factory) return factory();
  return name?.charAt(0)?.toUpperCase() || '';
}
