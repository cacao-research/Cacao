# ---------------------------------------------------------------------------
# Dockerfile generation
# ---------------------------------------------------------------------------

_DOCKERFILE_TEMPLATE = """\
# --- Cacao App ---
# Generated by: cacao docker {app_file}
FROM python:3.12-slim

WORKDIR /app

# Install system dependencies (none required for most apps)
RUN apt-get update && apt-get install -y --no-install-recommends \\
    && rm -rf /var/lib/apt/lists/*

# Install Python dependencies
COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt

# Copy application code
COPY . .

# Expose port
EXPOSE {port}

# Run the app
CMD ["cacao", "run", "{app_file}", "--host", "0.0.0.0", "--port", "{port}", "--no-reload"]
"""

_REQUIREMENTS_HEADER = "# Generated by: cacao docker\ncacao\nuvicorn[standard]\n"


def _detect_extra_deps(app_path: Path) -> list[str]:
    """Scan the app file for common import patterns and suggest extra deps."""
    extras: list[str] = []
    try:
        source = app_path.read_text(encoding="utf-8")
    except Exception:
        return extras

    dep_map: dict[str, str] = {
        "pandas": "pandas",
        "polars": "polars",
        "numpy": "numpy",
        "plotly": "plotly",
        "matplotlib": "matplotlib",
        "PIL": "pillow",
        "sklearn": "scikit-learn",
        "openai": "openai",
        "anthropic": "anthropic",
        "prompture": "prompture",
        "tukuy": "tukuy",
        "httpx": "httpx",
        "requests": "requests",
    }

    for import_name, pip_name in dep_map.items():
        if f"import {import_name}" in source or f"from {import_name}" in source:
            extras.append(pip_name)

    return extras


def _generate_dockerfile(
    app_file: str,
    port: int = 7860,
) -> str:
    """Generate a Dockerfile for the app."""
    return _DOCKERFILE_TEMPLATE.format(app_file=app_file, port=port)


def _generate_requirements(app_path: Path) -> str:
    """Generate requirements.txt content."""
    lines = [_REQUIREMENTS_HEADER.rstrip()]

    extras = _detect_extra_deps(app_path)
    for dep in extras:
        lines.append(dep)

    # Check if a requirements.txt already exists with extra deps
    existing = app_path.parent / "requirements.txt"
    if existing.exists():
        try:
            for line in existing.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if stripped and not stripped.startswith("#"):
                    # Normalize: take the package name (before any version specifier)
                    pkg_name = stripped.split(">=")[0].split("==")[0].split("[")[0].strip().lower()
                    if pkg_name not in ("cacao", "uvicorn") and stripped not in lines:
                        lines.append(stripped)
        except Exception:
            pass

    return "\n".join(lines) + "\n"


def _generate_dockerignore() -> str:
    """Generate .dockerignore content."""
    return """\
__pycache__
*.pyc
.git
.gitignore
.env
.venv
venv
node_modules
dist/
*.egg-info
.mypy_cache
.pytest_cache
.ruff_cache
"""


