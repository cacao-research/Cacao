def docker_command(args: list[str]) -> None:
    """
    Generate a Dockerfile for a Cacao app.

    Usage: cacao docker app.py [options]
    """
    parser = argparse.ArgumentParser(
        prog="cacao docker",
        description="Generate a Dockerfile for a Cacao application",
    )
    parser.add_argument("app_file", help="Path to the app file (e.g., app.py)")
    parser.add_argument(
        "--port", "-p", type=int, default=7860,
        help="Port to expose (default: 7860)",
    )
    parser.add_argument(
        "-o", "--output", default=".",
        help="Output directory (default: current directory)",
    )
    parser.add_argument(
        "--no-requirements", action="store_true",
        help="Don't generate requirements.txt",
    )

    parsed = parser.parse_args(args)
    app_path = Path(parsed.app_file)

    if not app_path.exists():
        print(f"{RED}Error: File '{parsed.app_file}' not found{RESET}")
        sys.exit(1)

    output_dir = Path(parsed.output)
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"{CYAN}Generating Docker files...{RESET}")

    # Generate Dockerfile
    dockerfile_content = _generate_dockerfile(parsed.app_file, port=parsed.port)
    dockerfile_path = output_dir / "Dockerfile"
    dockerfile_path.write_text(dockerfile_content, encoding="utf-8")
    print(f"  {GREEN}Created{RESET} Dockerfile")

    # Generate .dockerignore
    dockerignore_path = output_dir / ".dockerignore"
    if not dockerignore_path.exists():
        dockerignore_path.write_text(_generate_dockerignore(), encoding="utf-8")
        print(f"  {GREEN}Created{RESET} .dockerignore")
    else:
        print(f"  {DIM}Skipped{RESET} .dockerignore (already exists)")

    # Generate requirements.txt
    if not parsed.no_requirements:
        req_path = output_dir / "requirements.txt"
        if not req_path.exists():
            req_content = _generate_requirements(app_path)
            req_path.write_text(req_content, encoding="utf-8")
            print(f"  {GREEN}Created{RESET} requirements.txt")
        else:
            print(f"  {DIM}Skipped{RESET} requirements.txt (already exists)")

    print()
    print("To build and run:")
    print(f"  {CYAN}docker build -t my-cacao-app .{RESET}")
    print(f"  {CYAN}docker run -p {parsed.port}:{parsed.port} my-cacao-app{RESET}")
    print()


