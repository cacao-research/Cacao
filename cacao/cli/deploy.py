_DEPLOY_TARGETS = {
    "1": ("hf", "Hugging Face Spaces", "Free GPU/CPU, great for ML demos"),
    "2": ("docker", "Docker", "Generate Dockerfile for any container host"),
    "3": ("railway", "Railway", "One-click deploy from GitHub"),
    "4": ("render", "Render", "Free tier, auto-deploy from Git"),
    "5": ("flyio", "Fly.io", "Edge deployment, global distribution"),
    "6": ("ghpages", "GitHub Pages", "Static sites (already works via cacao build)"),
}


def _prompt_input(prompt: str, default: str = "") -> str:
    """Prompt user for input with optional default."""
    suffix = f" [{default}]" if default else ""
    try:
        value = input(f"  {prompt}{suffix}: ").strip()
    except (KeyboardInterrupt, EOFError):
        print("\nCancelled.")
        sys.exit(1)
    return value or default


def _get_app_title(app_path: Path) -> str:
    """Try to detect app title from the app file."""
    try:
        module = load_app_module(app_path)
        app_instance = find_app_instance(module)
        if hasattr(app_instance, "_title"):
            return str(app_instance._title)
    except Exception:
        pass
    return app_path.stem.replace("_", " ").replace("-", " ").title()


def _write_deploy_files(
    files: dict[str, str],
    output_dir: Path,
    force: bool = False,
) -> list[str]:
    """Write generated files to output directory. Returns list of written files."""
    written: list[str] = []
    for filename, content in files.items():
        filepath = output_dir / filename
        if filepath.exists() and not force:
            print(f"  {DIM}Skipped{RESET} {filename} (already exists)")
            continue
        filepath.write_text(content, encoding="utf-8")
        print(f"  {GREEN}Created{RESET} {filename}")
        written.append(filename)
    return written


def deploy_command(args: list[str]) -> None:
    """
    Deploy a Cacao app to the cloud with guided setup.

    Usage: cacao deploy app.py [target] [options]
    """
    parser = argparse.ArgumentParser(
        prog="cacao deploy",
        description="Deploy a Cacao app to the cloud",
    )
    parser.add_argument("app_file", help="Path to the app file (e.g., app.py)")
    parser.add_argument(
        "target",
        nargs="?",
        choices=["hf", "docker", "railway", "render", "flyio", "ghpages"],
        help="Deployment target (interactive if omitted)",
    )
    parser.add_argument(
        "--name", type=str, default=None,
        help="App/service name (auto-detected if omitted)",
    )
    parser.add_argument(
        "--port", "-p", type=int, default=None,
        help="Port to expose (target-specific default)",
    )
    parser.add_argument(
        "-o", "--output", default=".",
        help="Output directory (default: current directory)",
    )
    parser.add_argument(
        "--force", "-f", action="store_true",
        help="Overwrite existing files",
    )

    parsed = parser.parse_args(args)
    app_path = Path(parsed.app_file)

    if not app_path.exists():
        print(f"{RED}Error: File '{parsed.app_file}' not found{RESET}")
        sys.exit(1)

    if not app_path.suffix == ".py":
        print(f"{RED}Error: '{parsed.app_file}' is not a Python file{RESET}")
        sys.exit(1)

    # Print header
    print(_get_logo())
    print(f"  {DARK_BROWN}{'App':<12}{RESET}{app_path.resolve()}")
    print()

    # Select target
    target = parsed.target
    if not target:
        print(f"  {BOLD}Select deployment target:{RESET}")
        print()
        for key, (_, name, desc) in _DEPLOY_TARGETS.items():
            print(f"    {CYAN}{key}{RESET}  {name:<24} {DIM}{desc}{RESET}")
        print()

        choice = _prompt_input("Enter choice (1-6)")
        entry = _DEPLOY_TARGETS.get(choice)
        if not entry:
            print(f"{RED}Invalid choice: {choice}{RESET}")
            sys.exit(1)
        target = entry[0]

    # Resolve defaults
    app_file = parsed.app_file
    name = parsed.name or app_path.stem.replace("_", "-").lower()
    output_dir = Path(parsed.output)
    output_dir.mkdir(parents=True, exist_ok=True)

    # Dispatch to target
    if target == "ghpages":
        _deploy_ghpages(app_file)
        return

    if target == "docker":
        port = parsed.port or 7860
        print(f"  {BOLD}Generating Docker files...{RESET}")
        print()
        files: dict[str, str] = {
            "Dockerfile": _generate_dockerfile(app_file, port=port),
            ".dockerignore": _generate_dockerignore(),
            "requirements.txt": _generate_requirements(app_path),
        }
        _write_deploy_files(files, output_dir, force=parsed.force)
        print()
        print(f"  {BOLD}Next steps:{RESET}")
        print(f"    {CYAN}docker build -t {name} .{RESET}")
        print(f"    {CYAN}docker run -p {port}:{port} {name}{RESET}")

    elif target == "hf":
        port = parsed.port or 7860
        title = _get_app_title(app_path)
        print(f"  {BOLD}Generating Hugging Face Spaces files...{RESET}")
        print()
        files = _generate_hf_space(app_path, app_file, title, port=port)
        _write_deploy_files(files, output_dir, force=parsed.force)
        print()
        print(f"  {BOLD}Next steps:{RESET}")
        print(f"    1. Create a new Space at {CYAN}https://huggingface.co/new-space{RESET}")
        print(f"       - Select {BOLD}Docker{RESET} as the SDK")
        print(f"    2. Push these files to your Space repo:")
        print(f"       {CYAN}git init && git add .{RESET}")
        print(f"       {CYAN}git remote add origin https://huggingface.co/spaces/YOUR_USER/{name}{RESET}")
        print(f"       {CYAN}git push -u origin main{RESET}")

    elif target == "railway":
        port = parsed.port or 7860
        print(f"  {BOLD}Generating Railway files...{RESET}")
        print()
        files = _generate_railway(app_file, port=port)
        files["requirements.txt"] = _generate_requirements(app_path)
        _write_deploy_files(files, output_dir, force=parsed.force)
        print()
        print(f"  {BOLD}Next steps:{RESET}")
        print(f"    1. Install Railway CLI: {CYAN}npm i -g @railway/cli{RESET}")
        print(f"    2. Login: {CYAN}railway login{RESET}")
        print(f"    3. Deploy: {CYAN}railway up{RESET}")
        print(f"    {DIM}Or connect a GitHub repo at https://railway.app/new{RESET}")

    elif target == "render":
        port = parsed.port or 7860
        print(f"  {BOLD}Generating Render files...{RESET}")
        print()
        files = _generate_render(app_file, name, port=port)
        files["requirements.txt"] = _generate_requirements(app_path)
        _write_deploy_files(files, output_dir, force=parsed.force)
        print()
        print(f"  {BOLD}Next steps:{RESET}")
        print(f"    1. Push to GitHub")
        print(f"    2. Go to {CYAN}https://dashboard.render.com/new{RESET}")
        print(f"    3. Select {BOLD}Blueprint{RESET} and connect your repo")
        print(f"    {DIM}Render will auto-detect render.yaml and deploy{RESET}")

    elif target == "flyio":
        port = parsed.port or 8080
        print(f"  {BOLD}Generating Fly.io files...{RESET}")
        print()
        files = _generate_flyio(app_file, name, port=port)
        files["requirements.txt"] = _generate_requirements(app_path)
        _write_deploy_files(files, output_dir, force=parsed.force)
        print()
        print(f"  {BOLD}Next steps:{RESET}")
        print(f"    1. Install flyctl: {CYAN}curl -L https://fly.io/install.sh | sh{RESET}")
        print(f"    2. Login: {CYAN}fly auth login{RESET}")
        print(f"    3. Launch: {CYAN}fly launch --no-deploy{RESET}")
        print(f"    4. Deploy: {CYAN}fly deploy{RESET}")

    else:
        print(f"{RED}Unknown target: {target}{RESET}")
        sys.exit(1)

    print()


def _deploy_ghpages(app_file: str) -> None:
    """Handle GitHub Pages deployment (redirect to cacao build)."""
    print(f"  GitHub Pages uses static builds. Run:")
    print()
    print(f"    {CYAN}cacao build {app_file} --base-path /your-repo-name{RESET}")
    print()
    print(f"  Then deploy the {BOLD}dist/{RESET} folder to GitHub Pages.")
    print(f"  {DIM}See: https://pages.github.com/{RESET}")
    print()
