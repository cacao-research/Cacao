_HF_README_TEMPLATE = """\
---
title: {title}
emoji: 🤎
colorFrom: brown
colorTo: orange
sdk: docker
pinned: false
license: mit
---

# {title}

Built with [Cacao](https://github.com/cacao-research/Cacao) — a high-performance reactive web framework for Python.
"""


def _generate_hf_space(
    app_path: Path,
    app_file: str,
    title: str,
    port: int = 7860,
) -> dict[str, str]:
    """Generate files for Hugging Face Spaces deployment.

    Returns:
        Dict mapping filename -> content.
    """
    files: dict[str, str] = {}

    # Dockerfile (HF Spaces uses port 7860 by default)
    files["Dockerfile"] = _generate_dockerfile(app_file, port=port)
    files[".dockerignore"] = _generate_dockerignore()
    files["requirements.txt"] = _generate_requirements(app_path)
    files["README.md"] = _HF_README_TEMPLATE.format(title=title)

    return files


